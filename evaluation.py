import os
from typing import List, Optional

import pandas as pd
from datasets import Dataset
from ragas import evaluate
from ragas.metrics import faithfulness, answer_relevancy, context_precision, context_recall
from langchain_huggingface import HuggingFaceEndpoint, HuggingFaceEmbeddings


class RAGEvaluator:
    """Evaluate RAG pipeline using RAGAS metrics with Hugging Face models."""

    def __init__(
        self,
        hf_token: Optional[str] = None,
        model_name: str = "mistralai/Mistral-7B-Instruct-v0.3",
        embedding_model: str = "sentence-transformers/all-MiniLM-L6-v2",
    ):
        token = hf_token or os.environ.get("HF_TOKEN")
        if not token:
            raise ValueError("HF_TOKEN not found. Set it as env var or pass it to the constructor.")

        self.llm = HuggingFaceEndpoint(
            repo_id=model_name,
            temperature=0,
            huggingfacehub_api_token=token,
        )
        self.embeddings = HuggingFaceEmbeddings(
            model_name=embedding_model,
        )

        self.metrics = [
            faithfulness,
            answer_relevancy,
            context_precision,
            context_recall,
        ]

    def evaluate_rag(
        self,
        questions: List[str],
        answers: List[str],
        contexts: List[List[str]],
        ground_truths: Optional[List[str]] = None,
    ) -> dict:
        """Evaluate RAG responses using RAGAS metrics.
        
        Args:
            questions: List of questions asked
            answers: List of answers generated by the RAG pipeline
            contexts: List of context lists used for each answer
            ground_truths: Optional ground truth answers for recall metrics
            
        Returns:
            dict with evaluation results
        """
        if len(questions) != len(answers) or len(questions) != len(contexts):
            raise ValueError("questions, answers, and contexts must have the same length")

        if ground_truths is None:
            ground_truths = [""] * len(questions)

        data = {
            "question": questions,
            "answer": answers,
            "contexts": contexts,
            "ground_truth": ground_truths,
        }

        dataset = Dataset.from_dict(data)

        result = evaluate(
            dataset=dataset,
            metrics=self.metrics,
            llm=self.llm,
            embeddings=self.embeddings,
        )

        df = result.to_pandas()
        scores = result.scores

        return {
            "scores": {metric.name: score for metric, score in zip(self.metrics, scores)},
            "df": df,
            "summary": result,
        }

    def format_results(self, eval_result: dict) -> str:
        """Format evaluation results for display."""
        scores = eval_result["scores"]
        output = "## RAGAS Evaluation Results\n\n"
        output += "| Metric | Score |\n"
        output += "|--------|-------|\n"
        for metric_name, score in scores.items():
            output += f"| {metric_name} | {score:.4f} |\n"

        output += "\n### Detailed Results\n\n"
        df = eval_result["df"]
        output += df.to_string(index=False)

        return output

    def get_default_questions(self) -> List[str]:
        """Return default evaluation questions."""
        return [
            "What is the main topic of this document?",
            "What are the key points discussed?",
            "What conclusions are drawn?",
        ]
